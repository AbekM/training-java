package com.epam.training.Asilbek_Makhmudov.figures_extra;

class Point {
    public double x;
    public double y;
    public Point(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
}
